//! Security Audit Runner
//!
//! This module acts as an automated security audit harness. It aggregates
//! security findings across all test categories and prints a structured
//! security report.
//!
//! # Usage
//! ```bash
//! cargo test security_audit -- --nocapture
//! ```
//!
//! This will run all audit checks and print a formatted Security Report
//! to stdout categorized by severity.

#![cfg(test)]

use std::collections::HashMap;

// ─── Finding Severity ─────────────────────────────────────────────────────

#[derive(Debug, Clone, PartialEq, Eq, Hash)]
pub enum Severity {
    Critical,
    High,
    Medium,
    Low,
    Informational,
}

impl Severity {
    fn label(&self) -> &'static str {
        match self {
            Severity::Critical => "🔴 CRITICAL",
            Severity::High => "🟠 HIGH",
            Severity::Medium => "🟡 MEDIUM",
            Severity::Low => "🟢 LOW",
            Severity::Informational => "🔵 INFO",
        }
    }
    fn score(&self) -> u32 {
        match self {
            Severity::Critical => 10,
            Severity::High => 7,
            Severity::Medium => 4,
            Severity::Low => 1,
            Severity::Informational => 0,
        }
    }
}

// ─── Finding ──────────────────────────────────────────────────────────────

#[derive(Debug, Clone)]
pub struct SecurityFinding {
    pub id: String,
    pub title: String,
    pub description: String,
    pub severity: Severity,
    pub category: String,
    pub status: FindingStatus,
    pub recommendation: String,
}

#[derive(Debug, Clone, PartialEq)]
pub enum FindingStatus {
    Detected,    // Test found a real vulnerability
    Mitigated,   // Test confirms the protection is in place
    NeedsReview, // Behavior is ambiguous — needs manual review
}

impl FindingStatus {
    fn label(&self) -> &'static str {
        match self {
            FindingStatus::Detected => "❌ VULNERABILITY DETECTED",
            FindingStatus::Mitigated => "✅ MITIGATED",
            FindingStatus::NeedsReview => "⚠️  NEEDS REVIEW",
        }
    }
}

// ─── Audit Registry ──────────────────────────────────────────────────────

pub struct SecurityAudit {
    findings: Vec<SecurityFinding>,
}

impl SecurityAudit {
    pub fn new() -> Self {
        Self { findings: Vec::new() }
    }

    pub fn add(&mut self, finding: SecurityFinding) {
        self.findings.push(finding);
    }

    pub fn print_report(&self) {
        println!("\n");
        println!("╔══════════════════════════════════════════════════════════════╗");
        println!("║         PropChain Security Audit Report                      ║");
        println!("║         Generated by: security_audit_runner.rs               ║");
        println!("╚══════════════════════════════════════════════════════════════╝");
        println!();

        // Summary stats
        let mitigated = self.findings.iter().filter(|f| f.status == FindingStatus::Mitigated).count();
        let detected = self.findings.iter().filter(|f| f.status == FindingStatus::Detected).count();
        let review = self.findings.iter().filter(|f| f.status == FindingStatus::NeedsReview).count();
        let risk_score: u32 = self.findings.iter()
            .filter(|f| f.status == FindingStatus::Detected)
            .map(|f| f.severity.score())
            .sum();

        println!("━━━━━━━━━━━━━━━━━━━━━━━━━ SUMMARY ━━━━━━━━━━━━━━━━━━━━━━━━━━━━");
        println!("  Total Checks:       {}", self.findings.len());
        println!("  ✅ Mitigated:        {}", mitigated);
        println!("  ❌ Vulnerabilities:  {}", detected);
        println!("  ⚠️  Needs Review:    {}", review);
        println!("  Risk Score:         {}/100", risk_score.min(100));
        println!();

        // Group by category
        let mut by_category: HashMap<String, Vec<&SecurityFinding>> = HashMap::new();
        for f in &self.findings {
            by_category.entry(f.category.clone()).or_default().push(f);
        }

        let mut categories: Vec<&String> = by_category.keys().collect();
        categories.sort();

        for category in categories {
            let items = &by_category[category];
            println!("━━━━━━━━━━━━━━━━━━━━━━━━━ {} ━━━━━━━━━━", category.to_uppercase());
            for f in items {
                println!("  [{}] {} | {}", f.id, f.severity.label(), f.status.label());
                println!("       Title: {}", f.title);
                println!("       Desc:  {}", f.description);
                if f.status != FindingStatus::Mitigated {
                    println!("       Fix:   {}", f.recommendation);
                }
                println!();
            }
        }

        println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━");
        if detected == 0 {
            println!("  🎉 No vulnerabilities detected. All checks passed.");
        } else {
            println!("  ⚠️  {} issue(s) require immediate attention.", detected);
        }
        println!("━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n");
    }

    pub fn assert_no_critical(&self) {
        let critical_found: Vec<&SecurityFinding> = self.findings.iter()
            .filter(|f| f.severity == Severity::Critical && f.status == FindingStatus::Detected)
            .collect();
        assert!(
            critical_found.is_empty(),
            "AUDIT FAILED: {} critical vulnerabilities detected: {:?}",
            critical_found.len(),
            critical_found.iter().map(|f| &f.id).collect::<Vec<_>>()
        );
    }
}

